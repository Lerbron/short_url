const mysql = require('mysql2');
const config = require('./dbConfig')

const connection = mysql.createPool({
  host: config.host,
  port: config.port,
  user: config.user,
  password: config.password,
  database: config.database,
  waitForConnections: true,
  connectionLimit: 10,
  queueLimit: 0
});

module.exports= {
  connection
}